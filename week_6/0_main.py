
# This is good practice and should be followed!
# You will see this in future weeks a lot
def main():
    # This is the main function
    pass # Why is pass keyword used?

# This is checking whether this is the python file you called
# If it is, then the variable __name__ is set to "__main__"
# This file will not run if you call it from another file
# This is why it is good practice
if __name__ == "__main__":
    main()